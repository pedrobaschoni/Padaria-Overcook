    private void lConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lConfirmarMouseClicked

        if (tfNome.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Necessario colocar o nome");
            tfNome.requestFocus();

        } else if (tfLogin.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Necessario colocar o login");
            tfLogin.requestFocus();

        } else if (pfSenha.getPassword().equals(null)) {

            JOptionPane.showMessageDialog(null, "Necessario colocar a senha");
            pfSenha.requestFocus();

        } else if (tfEmail.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Necessario colocar o email");
            tfEmail.requestFocus();

        } else if (tfCpf.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Necessario colocar cpf");
            tfCpf.requestFocus();

        } else if (tfTelefone.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Necessario colocar o telefone");
            tfCpf.requestFocus();

        } else {

            if (rbAtendente.isSelected()) {

                
                funcionario.setNome(tfNome.getText());
                funcionario.setTipo(TipoFuncionario.Atendente);
                funcionario.setLogin(tfLogin.getText());
                funcionario.setSenha(String.valueOf(pfSenha.getPassword()));
                funcionario.setCpf(tfCpf.getText());
                funcionario.setStatusFuncionario(StatusFuncionario.Ativo);
                funcionario.setEmail(tfEmail.getText());
                funcionario.setTelefone(tfTelefone.getText());
                
                this.dispose();    
            } else if (rbCaixa.isSelected()) {

                funcionario.setNome(tfNome.getText());
                funcionario.setTipo(TipoFuncionario.Caixa);
                funcionario.setLogin(tfLogin.getText());
                funcionario.setSenha(String.valueOf(pfSenha.getPassword()));
                funcionario.setCpf(tfCpf.getText());
                funcionario.setStatusFuncionario(StatusFuncionario.Ativo);
                funcionario.setEmail(tfEmail.getText());
                funcionario.setTelefone(tfTelefone.getText());

                this.dispose();
                
            } else if (rbGerente.isSelected()) {

                funcionario.setNome(tfNome.getText());
                funcionario.setTipo(TipoFuncionario.Gerente);
                funcionario.setLogin(tfLogin.getText());
                funcionario.setSenha(String.valueOf(pfSenha.getPassword()));
                funcionario.setCpf(tfCpf.getText());
                funcionario.setStatusFuncionario(StatusFuncionario.Ativo);
                funcionario.setEmail(tfEmail.getText());
                funcionario.setTelefone(tfTelefone.getText());
                
                this.dispose();
                
            } else {

                JOptionPane.showMessageDialog(null, "Necessario colocar o tipo");
                rbGerente.requestFocus();
            }
        }
    }//GEN-LAST:event_lConfirmarMouseClicked

