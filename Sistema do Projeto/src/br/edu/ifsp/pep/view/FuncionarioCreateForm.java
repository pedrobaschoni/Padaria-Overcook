    public void setFuncionario(Funcionario funcionario) {
        
        this.funcionario.setId(funcionario.getId());
        tfNome.setText(funcionario.getNome());
        tfLogin.setText(funcionario.getLogin());
        pfSenha.setText(funcionario.getSenha());
        tfCpf.setText(funcionario.getCpf());
        tfEmail.setText(funcionario.getEmail());
        tfTelefone.setText(funcionario.getTelefone());

        switch (funcionario.getTipo()) {

            case Gerente:
                rbGerente.setSelected(true);
                break;

            case Atendente:
                rbAtendente.setSelected(true);
                break;

            case Caixa:
                rbCaixa.setSelected(true);
                break;
        }

    }

