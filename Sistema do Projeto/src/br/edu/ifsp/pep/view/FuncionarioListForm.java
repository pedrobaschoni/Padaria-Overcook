    private void bAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAlterarActionPerformed

        int posicao = tabela.getSelectedRow();

        if (posicao != -1) {

            FuncionarioCreateForm funcionarioCreateForm = new FuncionarioCreateForm(null, rootPaneCheckingEnabled);

            funcionarioCreateForm.setFuncionario(funcionarios.get(posicao));
            
            funcionarioCreateForm.setIsEditar(true);
            
            funcionarioCreateForm.setVisible(true);

            try {

                funcionarioDAO.alterar(funcionarioCreateForm.getFuncionario());
                bPesquisarActionPerformed(null);

            } catch (Exception ex) {

                ex.printStackTrace();
            }
        }else {

            JOptionPane.showMessageDialog(null,
                    "Selecione um Funcionario.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bAlterarActionPerformed

