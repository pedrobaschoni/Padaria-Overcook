    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed

        int posicao = tabela.getSelectedRow();

        if (posicao != -1) {

            Funcionario funcionarioSelecionado = funcionarios.get(posicao);

            if (!funcionarioSelecionado.equals(PrincipalForm.getFuncionarioLogado())) {

                int resposta = JOptionPane.showConfirmDialog(
                        null, "Confirma a exclusão?",
                        "Confirmação",
                        JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    funcionarioSelecionado.setStatusFuncionario(StatusFuncionario.Inativo);
                    funcionarioDAO.alterar(funcionarioSelecionado);

                    bPesquisarActionPerformed(null);

                } else {
                    JOptionPane.showMessageDialog(null,
                            "Não pode excluir o Funcionario conectado.",
                            "Aviso",
                            JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {

            JOptionPane.showMessageDialog(null,
                    "Selecione um Funcionario.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_bExcluirActionPerformed

    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed

        FuncionarioCreateForm funcionarioCreateForm = new FuncionarioCreateForm(null, rootPaneCheckingEnabled);

        funcionarioCreateForm.setVisible(true);

        if (funcionarioCreateForm.getFuncionario() != null) {

            try {

                funcionarioDAO.inserir(funcionarioCreateForm.getFuncionario());

            } catch (Exception ex) {

                ex.printStackTrace();
            }

            bPesquisarActionPerformed(null);
        }
    }//GEN-LAST:event_bCadastrarActionPerformed

