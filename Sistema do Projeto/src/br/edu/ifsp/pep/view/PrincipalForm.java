    private void moveView() {
        addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent e) {
                if (!e.isMetaDown()) {
                    point.x = e.getX();
                    point.y = e.getY();
                }
            }
        });
        addMouseMotionListener(new MouseMotionAdapter() {
            public void mouseDragged(MouseEvent e) {
                if (!e.isMetaDown() && point.y <= 17) {
                    Point p = getLocation();
                    setLocation(p.x + e.getX() - point.x, p.y + e.getY() - point.y);
                }
            }
        });
    }
    
    private void verificarFuncionario(){
        
        if(funcionarioLogado.getTipo() == TipoFuncionario.Caixa){
        
            lFuncionario.setVisible(false);
            lFornecedor.setVisible(false);
            lProduto.setVisible(false);
            lCompra.setVisible(false);
            lVenda.setVisible(false);
        }
        
        if(funcionarioLogado.getTipo() == TipoFuncionario.Atendente){
            
            lFuncionario.setVisible(false);
            lFornecedor.setVisible(false);
            lProduto.setVisible(false);
            lCaixa.setVisible(false);
            lCompra.setVisible(false);
            
        }
    }

