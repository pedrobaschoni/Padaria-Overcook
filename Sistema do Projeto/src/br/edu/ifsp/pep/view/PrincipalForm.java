    private void pesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pesquisaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String texto = pesquisa.getText().toUpperCase();
            switch (texto) {
                case "Fornecedor":
                    new FornecedorCreateForm(this, rootPaneCheckingEnabled).setVisible(true);
                    break;
                case "Produto":
                    new ProdutoListForm(this, rootPaneCheckingEnabled).setVisible(true);
                    break;
                case "Caixa":
                    new CaixaForm(this, rootPaneCheckingEnabled).setVisible(true);
                    break;
                case "Funcionario":
                    new FuncionarioListForm(this, rootPaneCheckingEnabled).setVisible(true);
                    break;
                case "Compra":
                    new CompraForm(this, rootPaneCheckingEnabled).setVisible(true);
                    break;
            }
        }
    }//GEN-LAST:event_pesquisaKeyPressed

