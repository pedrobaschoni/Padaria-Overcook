    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        for (CompraProduto cp : compraprodutos) {

            model.addRow(new Object[]{
                cp.getProduto(),
                fornecedorSelecionado,
                cp.getProduto().getPreco(),
                cp.getValor()
            });
        }
    }

    private void atualizaCompraProduto(CompraProduto compraProduto, Produto produto, Integer quantidade) throws Exception {
        if (compraProduto == null) {

            compraProduto = new CompraProduto(compra, produto, produto.getPreco() * quantidade, quantidade);
            this.compraprodutos.add(compraProduto);

        } else {

            compraProduto.setQuantidade(quantidade + compraProduto.getQuantidade());
            compraProduto.setValor((quantidade * produto.getPreco()) + compraProduto.getValor());
        }

    }

    private CompraProduto pesquisarCompraProduto(Produto produto) {
        for (CompraProduto cp : compraprodutos) {
            if (cp.getProduto().equals(produto)) {
                return cp;
            }
        }
        return null;
    }

