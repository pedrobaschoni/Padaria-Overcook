    private void bAdicionarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdicionarCompraActionPerformed

        if (this.produtoSelecionado != null) {

            Integer quantidade = (Integer) jsQuantidade.getValue();

            if (quantidade > produtoSelecionado.getQuantidade()) {

                JOptionPane.showMessageDialog(null,
                    "Quantidade invalida", "Alerta",
                    JOptionPane.WARNING_MESSAGE);

            }

            if (this.fornecedorSelecionado != null) {

                CompraProduto compraProduto = pesquisarCompraProduto(produtoSelecionado);

                try {
                    atualizaCompraProduto(compraProduto, produtoSelecionado, quantidade);
                    atualizarTabela();

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,
                        ex.getMessage(), "Alerta",
                        JOptionPane.WARNING_MESSAGE);
                }
            } else {

                JOptionPane.showMessageDialog(null,
                    "Selecione um fornecedor", "Alerta",
                    JOptionPane.WARNING_MESSAGE);
            }

        } else {

            JOptionPane.showMessageDialog(null,
                "Selecione um produto", "Alerta",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bAdicionarCompraActionPerformed

