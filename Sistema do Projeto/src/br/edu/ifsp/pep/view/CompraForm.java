    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed

        compra.setCompraProdutos(compraprodutos);
        compra.setDataCompra(LocalDate.now());
        compra.setFornecedor(fornecedorSelecionado);

        double valorTotal = 0;

        for (CompraProduto cp : compraprodutos) {
            valorTotal += cp.getValor();
        }
        compra.setValorTotal(valorTotal);

        List<Parcela> parcelas = new ArrayList<>();
        int quantidadeParcelas = (int) sParcelas.getValue();

        double valorParcela = valorTotal / quantidadeParcelas;
        LocalDate dataVencimento = LocalDate.now();

        for (int i = 1; i <= quantidadeParcelas; i++) {
            dataVencimento = dataVencimento.plusMonths(1);
            parcelas.add(new Parcela(dataVencimento, valorParcela, compra));
        }
        compra.setParcelas(parcelas);

        try {
            new CompraDAO().inserir(compra);
            JOptionPane.showMessageDialog(null, "Compra Registrada.");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Insira um Item",
                "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bCadastrarActionPerformed

    private void bRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRemoverActionPerformed
        int selectedRow = tabela.getSelectedRow();

        if(selectedRow != -1){

            compraprodutos.remove(selectedRow);

            this.atualizarTabela();
        }
        else{

            JOptionPane.showMessageDialog(null, "Selecione uma compra",
                "Atenção", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_bRemoverActionPerformed

