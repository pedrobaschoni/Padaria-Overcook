    private void bFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFinalizarActionPerformed
        venda.setItens(itemPedido);
        double valorTotal  = 0;
        for (ItemPedido ip : itemPedido) {
            valorTotal += ip.getPreco() * ip.getQuantidade();
        }
        venda.setTotalVenda(valorTotal);

        try {
            new VendaDAO().inserir(venda);
            JOptionPane.showMessageDialog(null, "Venda Registrada " + "\n"
                + "Senha do pedido: " + venda.getId());
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),
                "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bFinalizarActionPerformed

    private void bConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmarActionPerformed
        if (this.produtoSelecionado == null) {
            JOptionPane.showMessageDialog(null,
                "Escolha um Produto.", "Alerta",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        Integer quantidade = Integer.valueOf(sQuantidade.getValue().toString());

        ItemPedido itemPedido = pesquisarItemPedido(produtoSelecionado);
        try {
            atualizaItemPedido(itemPedido, produtoSelecionado, quantidade);
            atualizarTabela();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                ex.getMessage(), "Alerta",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bConfirmarActionPerformed

