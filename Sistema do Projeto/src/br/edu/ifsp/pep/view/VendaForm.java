    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tableProduto.getModel();
        model.setNumRows(0);
        for (ItemPedido ip : itemPedido) {

            model.addRow(new Object[]{ip.getProduto().getDescricao(),
                ip.getQuantidade(),
                ip.getPreco(),
                ip.getQuantidade() * ip.getPreco()
            });
        }
    }
    
    private ItemPedido pesquisarItemPedido(Produto produto) {
        for (ItemPedido ip : itemPedido) {
            if (ip.getProduto().equals(produto)) {
                return ip;
            }
        }
        return null;
    }

    private void atualizaItemPedido(ItemPedido itemPedido, Produto produto, Integer quantidade) throws Exception {
        if (itemPedido == null) {
            if (quantidade > produto.getQuantidade()) {
                throw new Exception("Quantidade Insuficiente em estoque.");
            }
            itemPedido = new ItemPedido(venda, produto, quantidade);
            this.itemPedido.add(itemPedido);
        } else {
            if (quantidade + itemPedido.getQuantidade() > produto.getQuantidade()) {
                throw new Exception("Quantidade Insuficiente em estoque.");
            }
            itemPedido.setQuantidade(quantidade + itemPedido.getQuantidade());
        }

    }


