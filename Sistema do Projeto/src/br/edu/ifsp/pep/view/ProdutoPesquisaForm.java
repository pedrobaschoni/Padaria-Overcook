    public void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tableProduto.getModel();
        model.setNumRows(0);
        
        for (Produto p : produtos) {
            model.addRow(new Object[]{p.getDescricao(), p.getQuantidade(), p.getPreco()});
        }
    }
    
