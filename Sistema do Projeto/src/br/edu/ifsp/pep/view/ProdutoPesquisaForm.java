    private void bPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPesquisaActionPerformed
        produtos = produtoDAO.buscarPorDescricao(tfPesquisa.getText());
        
        atualizarTabela();
    }//GEN-LAST:event_bPesquisaActionPerformed

    private void bCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCancelarActionPerformed
        this.produtoSelecionado = null;
        
        this.dispose();
    }//GEN-LAST:event_bCancelarActionPerformed

    private void bRetornarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRetornarActionPerformed
        if (tableProduto.getSelectedRow() > -1) {
            this.produtoSelecionado = produtos
                    .get(tableProduto.getSelectedRow());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto.", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bRetornarActionPerformed

